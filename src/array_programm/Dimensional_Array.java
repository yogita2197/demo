package array_programm;

public class Dimensional_Array {

	public static void main(String[] args) {
		int a[] = new int [5];
		a[0]=1;
		a[1]=2;
		a[2]=4;
		a[3]=45;
		a[4]=67;
		
		
		
		int[][] obj = new int[3][2];
		obj[0][0]= 3;
		obj[0][1] =5;
		obj[1][0]=23;
		obj[1][1]=45;
		obj[2][0]=56;
		obj[2][1]=89;
				

	}

}
